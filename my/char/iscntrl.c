///////////////////////////////////////////////////////////////////////////////
/// \brief Check if a character is a control character.
///
/// \param ch       The character to check.
///
/// \return         TRUE if the character is a control character, FALSE
///                 otherwise.
///
///////////////////////////////////////////////////////////////////////////////
bool_t my_iscntrl(int ch)
{
    return (BOOL((ch >= 0 && ch <= 31) || ch == 127));
}
