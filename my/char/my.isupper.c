#include "my.h"

///////////////////////////////////////////////////////////////////////////////
/// \brief Check if a character is a uppercase letter.
///
/// \param ch       The character to check.
///
/// \return         TRUE if the character is a uppercase letter, FALSE
///                 otherwise.
///
///////////////////////////////////////////////////////////////////////////////
bool_t my_isupper(int ch)
{
    return (BOOL(ch >= 'A' && ch <= 'Z'));
}
