#include "my.h"

///////////////////////////////////////////////////////////////////////////////
/// \brief Check if a character is a digit [0-9].
///
/// \param ch       The character to check.
///
/// \return         TRUE if the character is a digit, FALSE otherwise.
///
///////////////////////////////////////////////////////////////////////////////
bool_t my_isdigit(int ch)
{
    return (BOOL(ch >= '0' && ch <= '9'));
}
